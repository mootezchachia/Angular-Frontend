class Courses {
  final String id;
  final String name;
  final String description;
  final int price;
  final String requirement;
  final int duration;
  final String image;

  Courses({
    required this.id,
    required this.name,
    required this.description,
    required this.price,
    required this.requirement,
    required this.duration,
    required this.image,
  });
}
